internal class RockPaperScissors
{
    private readonly int Player1ResultScore;
    private readonly int Player2ResultScore;
    private readonly int Player1ChoiceScore;
    private readonly int Player2ChoiceScore;

    public int Player1Score => Player1ChoiceScore + Player1ResultScore;
    public int Player2Score => Player2ChoiceScore + Player2ResultScore;
    public RockPaperScissors(string input)
    {
        var player1Choice = input.Split(' ')[0];
        var player2Choice = input.Split(' ')[1];
        switch (player1Choice)
        {
            case "A":
                Player1ChoiceScore = 1;
                break;
            case "B":
                Player1ChoiceScore = 2;
                break;
            default:
                Player1ChoiceScore = 3;
                break;
        }
        switch (player2Choice)
        {
            case "X":
                Player2ChoiceScore = 1;
                break;
            case "Y":
                Player2ChoiceScore = 2;
                break;
            default:
                Player2ChoiceScore = 3;
                break;
        }
        if (Player1ChoiceScore == 1 && Player2ChoiceScore == 2
            || Player1ChoiceScore == 2 && Player2ChoiceScore == 3
            || Player1ChoiceScore == 3 && Player2ChoiceScore == 1)
        {
            // player 2 beats 1..
            Player1ResultScore = 0;
            Player2ResultScore = 6;
        }
        else if (Player1ChoiceScore == 1 && Player2ChoiceScore == 3
            || Player1ChoiceScore == 2 && Player2ChoiceScore == 1
            || Player1ChoiceScore == 3 && Player2ChoiceScore == 2)
        {
            // player 1 beats 2..
            Player1ResultScore = 6;
            Player2ResultScore = 0;
        }else{
            // it's a tie
            Player1ResultScore = 3;
            Player2ResultScore = 3;
        }
    }
}
internal class RockPaperScissorsPart2
{
    private readonly int Player1ResultScore;
    private readonly int Player2ResultScore;
    private readonly int Player1ChoiceScore;
    private readonly int Player2ChoiceScore;

    public int Player1Score => Player1ChoiceScore + Player1ResultScore;
    public int Player2Score => Player2ChoiceScore + Player2ResultScore;
    public RockPaperScissorsPart2(string input)
    {
        var player1Choice = input.Split(' ')[0];
        switch (player1Choice)
        {
            case "A":
                Player1ChoiceScore = 1;
                break;
            case "B":
                Player1ChoiceScore = 2;
                break;
            default:
                Player1ChoiceScore = 3;
                break;
        }
        var gameResult = input.Split(' ')[1];
        switch (gameResult)
        {
            case "X":
                // player 1 needs to win..
                Player2ChoiceScore = Player1ChoiceScore - 1 == 0 ? 3 : Player1ChoiceScore - 1;
                break;
            case "Z":
                // player 2 needs to win..
                Player2ChoiceScore = Player1ChoiceScore + 1 == 4 ? 1 : Player1ChoiceScore + 1;
                break;
            default:
                // we need a tie..
                Player2ChoiceScore = Player1ChoiceScore;
                break;
        }
        
        if (Player1ChoiceScore == 1 && Player2ChoiceScore == 2
            || Player1ChoiceScore == 2 && Player2ChoiceScore == 3
            || Player1ChoiceScore == 3 && Player2ChoiceScore == 1)
        {
            // player 2 beats 1..
            Player1ResultScore = 0;
            Player2ResultScore = 6;
        }
        else if (Player1ChoiceScore == 1 && Player2ChoiceScore == 3
            || Player1ChoiceScore == 2 && Player2ChoiceScore == 1
            || Player1ChoiceScore == 3 && Player2ChoiceScore == 2)
        {
            // player 1 beats 2..
            Player1ResultScore = 6;
            Player2ResultScore = 0;
        }else{
            // it's a tie
            Player1ResultScore = 3;
            Player2ResultScore = 3;
        }
    }
}

